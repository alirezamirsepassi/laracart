<?php

namespace Alireza\LaraCart\Models;

use Illuminate\Database\Eloquent\Model;

class QuoteCondition extends Model
{
    /*
    |--------------------------------------------------------------------------
    | GLOBAL VARIABLES
    |--------------------------------------------------------------------------
    */

    protected $table = 'quote_conditions';
    protected $primaryKey = 'quote_condition_id';
    protected $guarded = ['quote_condition_id'];
    protected $fillable = [
        'quote_id', 'name', 'type', 'value', 'order', 'attributes'
    ];
    protected $casts = [
        'attributes'    =>  'array'
    ];
    public $timestamps = true;


    /*
    |--------------------------------------------------------------------------
    | FUNCTIONS
    |--------------------------------------------------------------------------
    */

    /*
    |--------------------------------------------------------------------------
    | RELATIONS
    |--------------------------------------------------------------------------
    */

    public function quote()
    {
        return $this->belongsTo(Quote::class);
    }

    /*
    |--------------------------------------------------------------------------
    | SCOPES
    |--------------------------------------------------------------------------
    */

    /*
    |--------------------------------------------------------------------------
    | ACCESORS
    |--------------------------------------------------------------------------
    */

    /*
    |--------------------------------------------------------------------------
    | MUTATORS
    |--------------------------------------------------------------------------
    */

}
